#!/usr/bin/env node
/* gen-text-assets.mjs — Regenerate src/text-assets.mjs from the source .md files.
 *
 * Run whenever mcp/instructions.md or mcp/prompts/cost-interview.md changes:
 *   node scripts/gen-text-assets.mjs
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');

const instructions    = readFileSync(path.join(root, 'mcp/instructions.md'), 'utf8');
const interviewPrompt = readFileSync(path.join(root, 'mcp/prompts/cost-interview.md'), 'utf8');

function jsStringLiteral(s) {
  return '`' + s.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${') + '`';
}

const output = `/* text-assets.mjs — Inline text assets for the Worker bundle.
 *
 * AUTO-GENERATED by scripts/gen-text-assets.mjs — do not edit by hand.
 * Source: mcp/instructions.md + mcp/prompts/cost-interview.md
 * Regenerate: node scripts/gen-text-assets.mjs
 */

export const instructions = ${jsStringLiteral(instructions)};

export const interviewPrompt = ${jsStringLiteral(interviewPrompt)};
`;

const dest = path.join(root, 'src/text-assets.mjs');
writeFileSync(dest, output, 'utf8');
console.log(`wrote ${dest}`);
